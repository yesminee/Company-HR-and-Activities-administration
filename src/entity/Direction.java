package entity;
public class Direction {
	
	private int code;
	private String nom;
	private String email;

	public void setCode(int code) {
		this.code = code;
	}

	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	public int getCode() {
		return code;
	}
	
	
	
	
	
}
